class Example2 {
    public static void main(String[] args) {
        int num; //В этой строке объявляется переменная с именем num
        num = 100; //В этой строке кода переменной num присваивается значение 100
        System.out.println("Это переменная num: " + num);
        num = num * 2;
        System.out.println("Значение переменной num * 2 равно ");
        System.out.println(num);
    }
}
